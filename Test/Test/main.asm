.ORG 0

; CONFIG PA0, PA1, PA2 OUTPUT
	
	LDI R16, 7
	OUT DDRA, R16

;SOLUTION
LOOP:
	LDI R16, 4
	OUT PORTA, R16
	CALL DELAY_1S
	LDI R16, 5
	OUT PORTA, R16
	CALL DELAY_1S
	LDI R16, 3
	OUT PORTA, R16
	CALL DELAY_1S
	LDI R16, 4
	OUT PORTA, R16
	CALL DELAY_1S
	LDI R16, 5
	OUT PORTA, R16
	CALL DELAY_1S
	LDI R16, 1
	OUT PORTA, R16
	CALL DELAY_1S
	RJMP LOOP

DELAY_1S:
	LDI R20, 4
LP3:	LDI R21, 250
LP2:	LDI R22, 250
LP1:	NOP
	DEC R22
	BRNE LP1
	DEC R21
	BRNE LP2
	DEC R20
	BRNE LP3
	RET
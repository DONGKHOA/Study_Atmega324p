.ORG 0
LDI R16,0xFF
OUT DDRB,R16        ;port B
LDI R17,0
CLR R19

LOOP:
    LDI ZL,LOW(TABLE<<1)
    LDI ZH,HIGH(TABLE<<1)
    ADD ZL,R17
    ADC ZH,R19
    LPM R18,Z
    OUT PORTB,R18
    CALL DELAY_1S
    INC R17
    CPI R17,0x0A
    BREQ RESET
    RJMP LOOP

RESET:
    LDI R17,0
    RJMP LOOP


DELAY_1S:
    LDI R23,10
LP10:
    CALL DELAY_100MS
    DEC R23
    BRNE LP10
    RET

DELAY_100MS:
    LDI R22,2
LP3:    LDI R21,25
LP2:    LDI R20,250
LP1:    NOP
    DEC R20
    BRNE LP1
    DEC R21
    BRNE LP2
    DEC R22
    BRNE LP3
    RET

TABLE: .DB $3F, $06, $5B, $4F, $66, $6D, $7D, $07, $7F, $6F
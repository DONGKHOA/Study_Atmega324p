.ORG 0

; ADDR -> Z
; DATA OF ADDR -> R16
; SIZE -> R17
; COUNT NEG -> R19
; COUNT ZERO -> R20
; COUNT POS -> R21

;SOLUTION

	LDI ZL, 0
	LDI ZH, 2
	LD R17, Z
	CLR R19
	CLR R20
	CLR R21

LOOP:
	LD R16, -Z
	ORI R16, 0
	BREQ ZERO
	BRMI NEGA
	INC R21
	DEC R17
	BRNE LOOP

STORE:
	STS $240, R20
	STS $241, R19
	STS $242, R21

END: RJMP END

ZERO: INC R20
	DEC R17
	BRNE LOOP
	RJMP STORE

NEGA: INC R19
	DEC R17
	BRNE LOOP
	RJMP STORE